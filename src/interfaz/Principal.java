    private void cmdServirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdServirActionPerformed
        // TODO add your handling code here:
        sw = 1;
        
        if (txtMaxCafe.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite la capacidad maxima", "Error", 2);
            txtMaxCafe.requestFocusInWindow();
            sw = 0;
        } else if (txtActualCafe.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite la cantidad a servir", "Error", 2);
            txtActualCafe.requestFocusInWindow();
            sw = 0;
        }else {
            try{
                MaxCafe = Integer.parseInt(txtMaxCafe.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Ingrese la capacidad maxima correctamente", "Error", 2);
                txtMaxCafe.requestFocusInWindow();
                txtMaxCafe.selectAll();
                sw = 0;
            }
            try {
                ActualCafe = Integer.parseInt(txtActualCafe.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Ingrese la cantidad a servir correctamente", "Error", 2);
                txtActualCafe.requestFocusInWindow();
                txtActualCafe.selectAll();
                sw = 0;
            
        }
        if (sw == 1){
        ActualCafe = Integer.parseInt(txtActualCafe.getText());
        MaxCafe = Integer.parseInt(txtMaxCafe.getText());
        
        f2 = new Cafetera(MaxCafe, ActualCafe);
        
        
        f3 = c.ServirTaza(f2);
        txtResultado2.setText("" + f3.Mostrar());
        txtResultado1.setText("" + c.Mostrar());
        txtActualCafe.setText("");
        txtMaxCafe.setText("");
        cmdCrear.setEnabled(false);
        cmdAjustar.setEnabled(true);
        cmdMostrar.setEnabled(true);
        cmdLlenar.setEnabled(true);
        cmdServir.setEnabled(false);
        cmdVaciar.setEnabled(true);
        JOptionPane.showMessageDialog(this, "Taza de café servida exitosamente", "Informacion", 1);
        }
    }//GEN-LAST:event_cmdServirActionPerformed
    }
