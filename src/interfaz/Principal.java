    private void cmdCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCrearActionPerformed
        // TODO add your handling code here:
        
        sw = 1;
        
        if (txtActual.getText().trim().isEmpty() && txtMax.getText().trim().isEmpty()){
        c = new Cafetera();
        cmdCrear.setEnabled(true);
        cmdAjustar.setEnabled(true);
        cmdMostrar.setEnabled(true);
        cmdLlenar.setEnabled(true);
        cmdServir.setEnabled(true);
        cmdVaciar.setEnabled(true);
        } else {
            try {
                Max = Integer.parseInt(txtMax.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Ingrese la capacidad maxima correctamente", "Error", 2);
                txtMax.requestFocusInWindow();
                txtMax.selectAll();
                sw = 0;
            }
            try {
                Actual = Integer.parseInt(txtActual.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Ingrese la cantidad actual de la cafetera correctamente", "Error", 2);
                txtActual.requestFocusInWindow();
                txtActual.selectAll();
                sw = 0;
            }
            if (sw == 1){
        Actual = Integer.parseInt(txtActual.getText());
        Max = Integer.parseInt(txtMax.getText());
        c = new Cafetera(Max, Actual);
        cmdCrear.setEnabled(false);
        cmdAjustar.setEnabled(true);
        cmdMostrar.setEnabled(true);
        cmdLlenar.setEnabled(true);
        cmdServir.setEnabled(true);
        cmdVaciar.setEnabled(true);
        txtActual.setText("");
        txtMax.setText("");
        JOptionPane.showMessageDialog(this, "Cafetera creada exitosamente", "Informacion", 1);
        }
        }
        
    }//GEN-LAST:event_cmdCrearActionPerformed

